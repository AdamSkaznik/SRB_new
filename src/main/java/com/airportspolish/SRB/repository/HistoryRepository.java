package com.airportspolish.SRB.repository;

import com.airportspolish.SRB.model.History;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;

import java.util.List;

@Repository
public interface HistoryRepository extends JpaRepository<History, Long> {

    String zap_history = "SELECT * FROM history WHERE event_id = ?1";
    @Query(value = zap_history, nativeQuery = true)
    List<History> getAllByEventId(Long eventId);
}
